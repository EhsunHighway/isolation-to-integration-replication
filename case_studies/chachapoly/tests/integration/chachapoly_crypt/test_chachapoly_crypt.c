void test_chachapoly_crypt(struct chachapoly_ctx *ctx, const void *nonce,
                     const void *ad, int ad_len, void *input, int input_len,
                     void *output, void *tag, int tag_len, int encrypt,
                     const unsigned char *expected_ct, const unsigned char *expected_tag)
{
    chachapoly_crypt(ctx, nonce, ad, ad_len, input, input_len, output, tag, tag_len, encrypt);


    bool match = true;

    if (expected_ct) {
        for (int i = 0; i < input_len; i++) {
            if (output && ((unsigned char *)output)[i] != expected_ct[i]) {
                match = false;
            }
        }
    }

    if (expected_tag) {
        for (int i = 0; i < 16; i++) {
            if (tag && ((unsigned char *)tag)[i] != expected_tag[i]) {
                match = false;
            }
        }
    }

    if (match) {
        printf("Test passed.\n");
    } else {
        printf("Test failed.\n");
        // exit(1); // use this for mutation analysis
    }
}

