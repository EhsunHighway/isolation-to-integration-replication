void test_poly1305_update(struct poly1305_context *ctx, const unsigned char *m, size_t bytes, size_t expected_leftover, const unsigned char expected_buffer[POLY1305_BLOCK_SIZE]) {
    poly1305_update(ctx, m, bytes);

    bool passed = true;

    // Compare leftover value
    if (ctx->leftover != expected_leftover) {
        passed = false;
    }

    // Compare buffer contents up to ctx->leftover bytes
    for (size_t i = 0; i < ctx->leftover; i++) {
        if (ctx->buffer[i] != expected_buffer[i]) {
            passed = false;
        }
    }

    if (passed) {
        printf("Test passed.\n");
    } else {
        printf("Test failed.\n");
        // exit(1); // use this for mutation analysis
    }
}



